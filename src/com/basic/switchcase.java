package com.basic;

public class switchcase {
	public static void main(String args[]) {
		int choice=30;
		switch(choice) {
		case 10:
			System.out.println(34);
			break;
		case 20:
			System.out.println(23);
			break;
		case 30:
			System.out.println(56);
			break;
		case 40:
			System.out.println(45);
			break;
		
		}
		
	}

}
